        <h4 class="modal-title" id="myModalLabel">What is Mental Illness?</h4>
      </div>
      <div class="modal-body">
        <p>
			Mental illnesses are biologically based brain disorders.  These disorders can severely disrupt a person's thinking, feeling, moods, ability to relate to others and capacity for coping with the demands of life.
			<br><br><b>Mental illnesses are treatable brain disorders - they are not a judgment.</b>
			<br><br>Mental illnesses can affect persons of any age, race, religion, or income. Mental illnesses are not the result of personal weakness, lack of character, or poor upbringing. They cannot be overcome through "will power" and are not related to a person's "character" or intelligence.
			<br>Mental illnesses include such disorders as:
			<ul>
				<li><p>schizophrenia</p></li>
				<li><p>major depression</p></li>
				<li><p>bipolar disorder (also known as manic depression)</p></li>
				<li><p>schizoaffective disorder</p></li>
				<li><p>borderline personality disorder.</p></li>
				<li><p>obsessive-compulsive disorder</p></li>
			</ul>
			The following links provide more information on mental illnesses:
			<ul>
				<li><p><p><a target='_blank' href='http://www.nami.org/template.cfm?section=About_Mental_Illness'>About Mental Illness</a> - NAMI National page describing what is (and isn't) mental illness.</p></p></li>
				<li><p><a target='_blank' href='http://www.nami.org/Template.cfm?Section=By_Illness'>List of Mental Illnesses</a> - NAMI National web pages with detailed information on specific mental illnesses</p></li>
			</ul>
		</p>
